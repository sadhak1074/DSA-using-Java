public class variables_and_datatypes {
    public static void main(String[] args) {
         int a = 4;
         float b = 5.6f;
         char c = 'd';
         String d = "Hello World";
         boolean e = true;
         System.out.println(a);
         System.out.println(b);
         System.out.println(c);
         System.out.println(d);
         System.out.println(e);
    }
}
